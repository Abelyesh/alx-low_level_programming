#include "3-calc.h"
/**
 * op_add - add 2 integers
 * @a: integer 1
 * @a: integer 2
 * Return: integer with te result of the operation.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substract 2 integers
 * @a: integer 1
 * @a: integer 2
 * Return: integer with the result of the operation.
 */
int op_sub(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide 2 integers
 * @a: integer 1
 * @a: integer 2
 * Return: integer with the result of the operation.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - module 2 integers
 * @a: integer 1
 * @a: integer 2
 * Return: integer with the result of the operation.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
